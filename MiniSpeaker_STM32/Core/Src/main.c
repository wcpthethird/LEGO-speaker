  HAL_TIM_Base_Start(&htim2);
  HAL_TIM_Base_Start(&htim3);
  HAL_OPAMP_Start(&hopamp2);

  HAL_ADC_Start_DMA(&hadc1, (uint32_t*)adc1DataIn, BLOCK_SIZE);
  HAL_ADC_Start_DMA(&hadc2, (uint32_t*)&adc2Data, 1);
  HAL_DAC_Start_DMA(&hdac1, dacChannel, (uint32_t*)dacDataOut, BLOCK_SIZE, dacAlignment);

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
	vol = (float)adc2Data / adc2Res; // Value between 0 and 1; multiplier for volume
	// Apply filter and move DAC data to output
	for (int i=0; i<numBlocks; i++)
	{
	  // Apply filter in blocks
	  arm_fir_f32(&fir, inF32 + (i * FIR_BLOCK_SIZE), outF32 + (i * FIR_BLOCK_SIZE), FIR_BLOCK_SIZE);
	}
	// Move filtered data to output
	DAC_Process();
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
  }
